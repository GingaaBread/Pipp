package creation;

/**
 *  The TextStyle enumeration determines what font is used for a rendered text.
 *  For example, the italic text style changes the main font to its italic version.
 *
 * @author Gino Glink
 * @since 1.0
 * @version 1.0
 */
public enum TextStyle {

    /**
     *  The NORMAL text style refers to the default font used within the document.
     *  This is useful for sentences and paragraphs that should not stand out.
     */
    NORMAL,

    /**
     *  The ITALIC text style refers to the italic version of the main font.
     *  This is useful for quotations and emphasised text that should stand out from the default font.
     */
    ITALIC

}
