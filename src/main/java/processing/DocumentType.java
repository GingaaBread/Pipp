package processing;

/**
 *  The DocumentType enumeration declares the allowed document types that a document can have.
 *  The type is mainly used for the document metadata, but its use is reserved for more complex future matters.
 *
 * @author Gino Glink
 *  @since 1.0
 *  @version 1.0
 */
public enum DocumentType {

    /**
     *  PAPER refers to a scientific paper, which is usually formulated in an academic style like MLA.
     */
    PAPER,

    /**
     *  BOOK refers to any type of written book, such as a novel or non-fictional book, for example.
     */
    BOOK

}
