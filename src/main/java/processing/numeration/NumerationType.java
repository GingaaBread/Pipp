package processing.numeration;

/**
 * The numeration type class determines in which numeration system the numbers are displayed.
 *
 * @version 1.0
 * @since 1.0
 */
public enum NumerationType {

    /**
     * The Arabic numeration type numbers the pages in Arabic numbers,
     * starting at 1, counting up. (1,2,3,...)
     */
    ARABIC,

    /**
     * The Roman numeration type numbers the pages in Roman numbers,
     * starting at I, counting up. (I, II, III, ...)
     */
    ROMAN

}
